<h3 class="heading3">Why Play Baseball ?</h3>
<p class="depart_p">
Baseball helps you grow up - both in mind and body. It brings you new friends, shows you how to get along with others, and helps you live up to your Cub Scout ideals - the Cub scout Promise and the Law of the Pack. </br> </br>

If you find you like baseball a lot, you may want to join a youth league or try out for your school team. When you are ready for college, you may be able to get a baseball scholarship. Some may gain added experience while playing for the State/Indian Team which represents the country in international competition and in the Olympics. And for some, the goal may even be to play professional ball someday. </br> </br>

But whether you play baseball just for fun or go for a career in the big leagues, remember that the best you can do is to do your best. </br> </br>

The Merit certificate of participation in Sub Junior / Junior / Senior, national championship will help you to get admission for higher studies in your respective states.</br> </br>

In International participation certificate will be eligible for Form I</br>

In National participation certificate will be eligible for Form II </br>

If you  want to know more about any specific youth leagues or details about baseball itself, contact the Amateur Baseball Federation of India at the address below. </br>

The Amateur Baseball Federation of India  (ABFI) is the national governing body for International Baseball Association, Baseball Federation of Asia. The ABFI represents the interests of all amateur baseball players and associations on the Indian Olympic committee and the International Association of Amateur Baseball (AINBA) They are responsible for the selection, training, and fielding of the Indian team for international competition annually. Including the world championships, the pan American games, and the Olympic games. The ABFI protects the rights and statues of amateur baseball players and provides services unique and necessary to all fans and players. </br>

You may contact the Amateur Baseball Federation by writing or calling. </br> </br>

