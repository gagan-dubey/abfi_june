				<h3 class="heading3">Amateur Baseball Federation of India</h3>
				<p class="depart_p">The Federation was established in December 1983 with an object to promote this game. On 11th December 1983 some Baseball lovers from Delhi, Manjpur, Haryana, Gujarat, M.P and West Bengal met at Rohtak (Haryana) and agreed to form a baseball federation to promote baseball game through out the country. The Baseball Leaders also agreed that the Federation should become a member of Asian baseball Federation and International Baseball Association. Persons at that time were Mr.P.C. Bhardwaj ( Delhi ) and Mr. E.Bijoy Singh (Manipur). Mr.P.C.Bhardwaj (Delhi) was appointed as Hony. Secretary and given responsibility to approach Asian and International Baseball Authority for membership. 1984 Officially form ABFI along with Tamilnadu, Karnataka, Andhra Pradesh and Punjab Baseball Aassociation.</p>


