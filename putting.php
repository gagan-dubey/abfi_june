<h3 class="heading3070">5.00 Putting The Ball In Play. Live Ball.</h3>
<p class="depart_p">

                        </br></br><strong>5.01</strong><br />
                          At the time set for beginning the game the umpire shall call &quot;Play.&quot; <br />
                          <br />
                          <strong>5.02</strong><br />
                          After the umpire calls &quot;Play&quot; the ball is alive and in play and remains  alive and in play until for legal cause, or at the umpire's call of  &quot;Time&quot; suspending play, the ball becomes dead. While the ball is dead  no player may be put out, no bases may be run and no runs may be  scored, except that runners may advance one or more bases as the result  of acts which occurred while the ball was alive (such as, but not  limited to a balk, an overthrow, interference, or a home run or other  fair ball hit out of the playing field). Should a ball come partially  apart in a game, it is in play until the play is completed. <br />
                          <br />
                          <strong>5.03</strong><br />
                          The pitcher shall deliver the pitch to the batter who may elect to  strike the ball, or who may not offer at it, as he chooses. <br />
                          <br />
                          <strong>5.04</strong><br />
                          The offensive team's objective is to have its batter become a runner, and its runners advance.<br />
                          <br />
                          <strong>5.05</strong><br />
                          The defensive team's objective is to prevent offensive players from  becoming runners, and to prevent their advance around the bases.<br />
                          <br />
                          <strong> 5.06</strong><br />
                          When a batter becomes a runner and touches all bases legally he shall  score one run for his team. A run legally scored cannot be nullified by  subsequent action of the runner, such as but not limited to an effort  to return to third base in the belief that he had left the base before  a caught fly ball.<br />
                          <br />
                          <strong>5.07</strong><br />
                          When three offensive players are legally put out, that team takes the  field and the opposing team becomes the offensive team. <br />
                          <br />
                          <strong>5.08</strong><br />
                          If a thrown ball accidentally touches a base coach, or a pitched or  thrown ball touches an umpire, the ball is alive and in play. However,  if the coach interferes with a thrown ball, the runner is out. <br />
                          <br />
                          <strong>5.09</strong><br />
                          The ball becomes dead and runners advance one base, or return to their  bases, without liability to be put out, when_ <br />
                          (a) A pitched ball touches a batter, or his clothing, while in his  legal batting position; runners, if forced, advance; <br />
                          (b) The plate umpire interferes with the catcher's throw; runners may not advance. <br />
                          NOTE: The interference shall be disregarded if the catcher's throw retires the runner. <br />
                          (c) A balk is committed; runners advance; (See Penalty 8.05). <br />
                          (d) A ball is illegally batted; runners return;<br />
                          (e) A foul ball is not caught; runners return. The umpire shall not put  the ball in play until all runners have retouched their bases; <br />
                          (f) A fair ball touches a runner or an umpire on fair territory before  it touches an infielder including the pitcher, or touches an umpire  before it has passed an infielder other than the pitcher; If a fair  ball touches an umpire working in the infield after it has bounded  past, or over, the pitcher, it is a dead ball. If a batted ball is  deflected by a fielder in fair territory and hits a runner or an umpire  while still in flight and then caught by an infielder it shall not be a  catch, but the ball shall remain in play. If a fair ball goes through,  or by, an infielder, and touches a runner immediately back of him, or  touches a runner after being deflected by an infielder, the ball is in  play and the umpire shall not declare the runner out. In making such  decision the umpire must be convinced that the ball passed through, or  by, the infielder and that no other infielder had the chance to make a  play on the ball; runners advance if forced; <br />
                          (g) A pitched ball lodges in the umpire's or catcher's mask or  paraphernalia, and remains out of play, runners advance one base; If a  foul tip hits the umpire and is caught by a fielder on the rebound, the  ball is &quot;dead&quot; and the batsman cannot be called out. The same shall  apply where such foul tip lodges in the umpire's mask or other  paraphernalia. If a third strike (not a foul tip) passes the catcher  and hits an umpire, the ball is in play. If such ball rebounds and is  caught by a fielder before it touches the ground, the batsman is not  out on such a catch, but the ball remains in play and the batsman may  be retired at first base, or touched with the ball for the out. If a  pitched ball lodges in the umpire's or catcher's mask or paraphernalia,  and remains out of play, on the third strike or fourth ball, then the  batter is entitled to first base and all runners advance one base. If  the count on the batter is less than three balls, runners advance one  base. <br />
                          (h) Any legal pitch touches a runner trying to score; runners advance. <br />
                          <br />
                          <strong>5.10</strong><br />
                          The ball becomes dead when an umpire calls &quot;Time.&quot; The umpire in chief shall call &quot;Time&quot;_ <br />
                          (a) When in his judgment weather, darkness or similar conditions make immediate further play impossible;<br />
                          (b) When light failure makes it difficult or impossible for the umpires to follow the play; <br />
                          NOTE: A league may adopt its own regulations governing games interrupted by light failure.<br />
                          (c) When an accident incapacitates a player or an umpire;<br />
                          (1) If an accident to a runner is such as to prevent him from  proceeding to a base to which he is entitled, as on a home run hit out  of the playing field, or an award of one or more bases, a substitute  runner shall be permitted to complete the play.<br />
                          (d) When a manager requests &quot;Time&quot; for a substitution, or for a conference with one of his players. <br />
                          (e) When the umpire wishes to examine the ball, to consult with either manager, or for any similar cause. <br />
                          (f) When a fielder, after catching a fly ball, falls into a bench or  stand, or falls across ropes into a crowd when spectators are on the  field. As pertains to runners, the provisions of 7.04 (c) shall  prevail. If a fielder after making a catch steps into a bench, but does  not fall, the ball is in play and runners may advance at their own  peril. <br />
                          (g) When an umpire orders a player or any other person removed from the playing field. <br />
                          (h) Except in the cases stated in paragraphs (b) and (c) (1) of this  rule, no umpire shall call &quot;Time&quot; while a play is in progress. <br />
                          <br />
                          <strong>5.11</strong><br />
                          After the ball is dead, play shall be resumed when the pitcher takes  his place on the pitcher's plate with a new ball or the same ball in  his possession and the plate umpire calls &quot;Play.&quot; The plate umpire  shall call &quot;Play&quot; as soon as the pitcher takes his place on his plate  with the ball in his possession.
