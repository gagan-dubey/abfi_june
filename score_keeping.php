<p class="depart_p"><img class="ks2" src="images/ks2.jpg">If the hitter grounds out to shortstop, for example, write in "6-3," which shows the shortstop threw him out at first base. If the hitter flies out to left field, write a "7." If the batter gets a hit, write in the hit according to which base he reached. Each corner of the box represents a base, with the lower-right corner being first. If he singles, put a "-" in the lower right. If he doubles, write a "=" in the upper right, and so on. For a walk, use "BB" in the lower right. As the runner advances, mark the appropriate symbol in the appropriate corner </br></br>If a runner scores, put a circle at the bottom of the box, and inside the circle put the symbol of the play and/or the player that drove him in. For example, if the No. 5 hitter drives in two runs with a single, mark his single in the bottom right of his box and mark a circle with the number "5" in it in the boxes of the runners who score (Some people like to use uniform numbers here, so you can tell who did what, even after lineup changes). At the end of each inning, total the hits and runs for that inning only. At the end of the game you'll be able to add the innings total to get the game score.</p>

