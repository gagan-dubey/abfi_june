							<p class="depart_p">Ist National Baseball  Championship was organized by Baseball Association, Delhi from 25th Feb to 28th Feb 1985 at New Delhi. The Federation also introduced this game for women. A.B.F.I was deeply grateful to B.F.A for providing the Baseball equipment.</p>
							<p class="depart_p">The Federation is determined to promote Baseball game through out the country. At present <strong>26</strong> States/UT are the affiliated member of the Federation. The federation has organized <strong>30</strong> Senior National Baseball Championship, <strong>25</strong> Junior National Championship, <strong>24</strong> Sub-junior National Championship, apart from <strong>13</strong> National Little League Tournament between 11-13 years age group. The Indian team won Bronze medal in 1995 & Silver medal in 1999 in the Asian Cup Baseball championship at Internationals Level. Indian team also participated in "AA" and "AAA" Championship held at Seoul and Taiwan in which Indian team performance was appreciable. The federation has been sending participants under 11 years in the world children Baseball Fair on the invitation from WCBF. The Indian woman team recently participated in 3rd woman world cup at Matsuyama, Japan in 2008.</strong></p>
							<p class="depart_p">The Baseball game has been included in the school game by the <strong>School Games Federation of India</strong> since 1998. The Federation succeeded to introduced Baseball game at Grass Route Level. Sports Authority of India has introduced certificate course for Baseball Coaches at N.I.S Patiala. </p>
							<p class="depart_p">A.B.F.I.'s most urgent task is to make Baseball Tremendously popular in India. The Federation will do its level best to achieve the goal</p>
