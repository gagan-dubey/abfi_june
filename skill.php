<label class="bol" style="margin-top:20px; font-size:26px;">Skill 1- Throwing Ball</label></br></br>
<p class="depart_p">
One of the most important things you do in baseball is to throw the ball. To throw accurately. You first must know how to grip the ball.  </br></br>
<label class="bol">HOW TO GRIP THE BALL </label></br>

Hold your index and middle fingers on top of the ball slightly speread across the seams.</br>
Hold your thumb directly </br>
Curl your ring finger and little finger undernearth</br>
Ball should rest on the flesh of the fingers but not against the palm</br>
There should be an opening between the base of your thumb and the ball</br>
Hold the ball firmly but not tight </br></br>

<label class="bol">But do not : </label></br>

Place too many fingers on top of the ball.</br>
Split the fingers too far apart.</br>
Seat the ball in the palm of the hand.</br>
Grip the ball too tight.  </br></br>

<label class="bol">HOW TO THROW: </label></br>

For best results, the ball must be thrown overhand.</br> </br> 

&nbsp;&nbsp;&nbsp;* Look at a target, usually the glove of  the player receiving the throw.</br>
&nbsp;&nbsp;&nbsp;* Step directly toward the target.</br>
&nbsp;&nbsp;&nbsp;* At the same time rotate the hips inward.</br>
&nbsp;&nbsp;&nbsp;* Bring the ball up at right angles to the elbow, which is parallel to the shoulder.</br>
&nbsp;&nbsp;&nbsp;* Cock the arm.</br>
&nbsp;&nbsp;&nbsp;* Push off the rear foot.</br>
&nbsp;&nbsp;&nbsp;* Throw overhand, releasing the ball with a downward wrist snap.</br>
&nbsp;&nbsp;&nbsp;* Follow through by bending the back and body forward. Let your rear foot drag. </br></br>

Develop a smooth Rhythm look, Step, Rotate, Cock the Arm, Throw, and follow through. </br></br>

On longer throws you can use a shuffle-hop step: step with the lead foot (opposite from the throwing hand) then take a short hop step with the rear foot, then step again with the lead foot. With a little practice, this will become automatic  </br></br>

<label class="bol">But do Not: </label></br>

&nbsp;&nbsp;&nbsp;* Throw with the elbow lower than the shoulder.</br>
&nbsp;&nbsp;&nbsp;* Fail to keep your eyes on the target.</br>
&nbsp;&nbsp;&nbsp;* Fail to follow through.</br>
</p>                                    
