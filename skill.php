<label class="bol" style="margin-top:20px; font-size:26px;">Skill 3 - Hitting</label></br></br>
<p class="depart_p">
<label class="bol">Hitting The Ball</label></br>

The most exciting part of baseball is hitting the ball. It also is the hardest thing to do. To do it well takes lots of practice in the skills you are learning. You must also try to be an aggressive hitter. This means you go up to bat to get a hit-and not to strike out or get a base on balls. </br></br>

<label class="bol">CHOOSE THE RIGHT BAT</label></br>

You hit better if you`re using a bat that`s just right for you. So choose a bat that`s light and feels good  when you swing it. Bats will feel different to different players, so each player should select his own bat. You should be able to start a swing without any effort. In other words, you control the bat. Don`t let the bat control you. </br></br>

<label class="bol">HOW TO GRIP THE BAT</label></br>

Gripping the bat right is very important. Hold it firmly with both brands. The first finger and the thumb of each hand should hold the other three fingers in place. Do not hold the bat in your palm or against your thumb. The middle Knuckles of your hands should be in line when gripping the bat. When you hold it properly, your arms and the hands will form a Vita`s a good idea to take a "choke" grip on the bat, which means holding it an inch or so from the knob end. </br>

Your Position in the batter`s box</br>

The batter`s box is where the batter stands when he bats. You should select your own best spot, and not try to copy someone else. Your position should let you cover the outside of the plate when you bat. You should be back far enough to let you watch the ball longer. Because the longer you can watch the ball, the better chance you have of hitting it. </br></br>

<label class="bol">DO</label></br>

&nbsp;&nbsp;&nbsp;* Place your front foot just behind  the edge of the plate.</br>
&nbsp;&nbsp;&nbsp;* Place your rear foot near the back line of the box.</br>
&nbsp;&nbsp;&nbsp;* Keep both feet parallel with each other and  Pointing toward the plate. Your front foot should point slightly toward the pitcher.</br>
&nbsp;&nbsp;&nbsp;* Your feet should be spread wider than your shoulders. A wide stance helps you stand steady and well balanced.</br></br> 

<label class="bol">But do not </label></br>

&nbsp;&nbsp;&nbsp;* Stand in front of the plate.</br>
&nbsp;&nbsp;&nbsp;* Place one foot in front of the other.</br>
&nbsp;&nbsp;&nbsp;* Stand too far away from the plate.</br>
&nbsp;&nbsp;&nbsp;* Bat with your feet together. </br></br>

<label class="bol">YOUR BATTING STANCE</label></br>

While waiting for the pitch, stand so that you`re comfortable and relaxed. Try to distribute your weight equally on both feet. Keep your eyes on the ball at all times. </br></br>

<label class="bol">Do: </label></br>

  Hold your bat with the hands at the top of the strike zone-which is the space between your knees and your armpits.</br>
&nbsp;&nbsp;&nbsp;* Keep your hands away from your body.</br>
&nbsp;&nbsp;&nbsp;* Hold your hands back over your rear foot.</br>
&nbsp;&nbsp;&nbsp;* Keep the bat almost parallel to the ground, not sticking up.</br>
&nbsp;&nbsp;&nbsp;* Hold your head steady with your chin on the front shoulder.</br>
&nbsp;&nbsp;&nbsp;* Bend your knees slightly</br>
&nbsp;&nbsp;&nbsp;* Keep your eyes on the ball. </br></br>

<label class="bol">THE SWING</label></br>

&nbsp;&nbsp;&nbsp;* As the pitcher releases the ball, tighten your grip to be ready to swing</br>
&nbsp;&nbsp;&nbsp;* As the ball approaches, hold steady-don`t move your bat or hands.</br>
&nbsp;&nbsp;&nbsp;* Take a short stride toward the pitcher, rotate the hips, and swing.</br>
&nbsp;&nbsp;&nbsp;* Watch the ball as it hits the bat.</br>
&nbsp;&nbsp;&nbsp;* Complete your swing and follow through.</br>
&nbsp;&nbsp;&nbsp;* Your chin should now rest on the opposite shoulder. </br></br>

<label class="bol">But do not</label></br>

&nbsp;&nbsp;&nbsp;* Stand stiff and rigid.</br>
&nbsp;&nbsp;&nbsp;* Hold the bat low.</br>
&nbsp;&nbsp;&nbsp;* Hold the barrel ( thick part ) of the bat at right angles.</br>
&nbsp;&nbsp;&nbsp;* Move the bat before you swing.</br>
&nbsp;&nbsp;&nbsp;* Turn your head as you swing.</br>
&nbsp;&nbsp;&nbsp;* Take a long stride. </br>
</p>
                                    
