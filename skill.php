<label class="bol" style="margin-top:20px; font-size:26px;">Skill 2 - Catching</label></br></br>
<p class="depart_p">When you are waiting for a ball to be hit in your direction take a comfortable position that will let you move quickly in any direction you neet to. This is called the "ready position" Here`s how you do it. </br>

&nbsp;&nbsp;&nbsp;* Spread your feet a little wider than your hips. Keep them parallel and pointing directly ahead.</br>
&nbsp;&nbsp;&nbsp;* Put your weight on the balls of your feet. Don`t stand flat footed.</br>
&nbsp;&nbsp;&nbsp;* Bend your knees slightly, in a half-crouch.</br>
&nbsp;&nbsp;&nbsp;* Keep your back parallel to the ground.</br>
&nbsp;&nbsp;&nbsp;* Keep your buttocks up, about even with your knees.</br>
&nbsp;&nbsp;&nbsp;* Keep your head up.</br>
&nbsp;&nbsp;&nbsp;* Hold your glove and free hand in front of your knees. </br> </br>


<label class="bol">HERE COMES THE BALL</label></br>

&nbsp;&nbsp;&nbsp;* Push your fingers well into the glove, so you can catch the ball in the glove pocket.</br>
&nbsp;&nbsp;&nbsp;* Always try to catch the ball in front of your body.</br>
&nbsp;&nbsp;&nbsp;* Always try to catch the ball with two hands.</br>
&nbsp;&nbsp;&nbsp;* Always reach forward to catch a ball, keeping the hands away from the body.</br>
&nbsp;&nbsp;&nbsp;* Let your hands "give" as the ball hits the glove pocket.</br>
&nbsp;&nbsp;&nbsp;* Watch the ball as it smacks into your glove.</br>
&nbsp;&nbsp;&nbsp;* To catch a ball on the side opposite your glove hand. Make a backhand catch. Reach across your body and turn the  glove outward so it faces the approaching ball. Back of glove should be facing you.</br></br>

<label class="bol">But Do Not:</label></br>

&nbsp;&nbsp;&nbsp;* Catch one-handed.</br>
&nbsp;&nbsp;&nbsp;* Catch the ball in the webbing of the glove.</br>
&nbsp;&nbsp;&nbsp;* Hold your hands rigid or stiff.</br>
&nbsp;&nbsp;&nbsp;* Take your eyes off the ball.</br>
&nbsp;&nbsp;&nbsp;* Hold fingers up on low balls.</br>
&nbsp;&nbsp;&nbsp;* Hold fingers down on high balls. </br></br>

<label class="bol">HOW TO CATCH A GROUND BALL</label></br>

&nbsp;&nbsp;&nbsp;* Move toward it fast. Never back up on a ground ball.</br>
&nbsp;&nbsp;&nbsp;* Get directly in front of the ball, then stop.</br>
&nbsp;&nbsp;&nbsp;* Place your hands on the ground in front of your body, and the path of the approaching ball.</br>
&nbsp;&nbsp;&nbsp;* Try to catch the ball on the ground, at the top of its bounce, or on its downward flight-but Never as it comes up.</br>
&nbsp;&nbsp;&nbsp;* Keep your eye on the ball as it hits your glove.</br>
&nbsp;&nbsp;&nbsp;* Grip the ball with the bare hand, preparing for a throw.</br>
&nbsp;&nbsp;&nbsp;* Point fingers of the glove and belt.</br>
&nbsp;&nbsp;&nbsp;* Straighten up, take the shuffle-hop-step, and throw the ball as fase as you can. </br></br>

<label class="bol">But do not </label></br>

&nbsp;&nbsp;&nbsp;* Field the ball while you are moving</br>
&nbsp;&nbsp;&nbsp;* Field the ball with one foot in front of the other.</br>
&nbsp;&nbsp;&nbsp;* Turn your head as the ball approaches you.</br>
&nbsp;&nbsp;&nbsp;* Go down on one knee to field the ball. </br></br>

<label class="bol">HOW TO CATCH A FLY BALL </label></br>

&nbsp;&nbsp;&nbsp;* Get under the ball as soon as possible. This lets you adjust to the sun, wind, and the ball`s speed and direction.</br>
&nbsp;&nbsp;&nbsp;* Catch the ball in front of and over the shoulder of your throwing hand.</br>
&nbsp;&nbsp;&nbsp;* Keep your glove and fingers pointing up.</br>
&nbsp;&nbsp;&nbsp;* Catch the ball with two hands.</br>
&nbsp;&nbsp;&nbsp;* When you catch it, throw immediately, using the shuffle-hop-step as you throw. </br></br>


<label class="bol">But do not </label></br>

&nbsp;&nbsp;&nbsp;* Try to catch one-handed.</br>
&nbsp;&nbsp;&nbsp;* Reach out for the ball at the last moment.</br>
</p>
                                    
