<label class="bol" style="margin-top:20px; font-size:26px;">Skill 4 - Running</label></br></br>
<p class="depart_p">
<label class="bol">Base Running</label></br>
&nbsp;&nbsp;&nbsp;* When hit the ball, drop the bat and run like a sprinter, pumping your arms at your side, with your body learning forward and knees raised high.</br>
&nbsp;&nbsp;&nbsp;* Keep your eyes straight ahead, looking at the base and listening to the first base coach.</br>
&nbsp;&nbsp;&nbsp;* Run full speed past the base after touching it.</br>
&nbsp;&nbsp;&nbsp;* Do not lap or jump at the base. Keep your stride.</br>
&nbsp;&nbsp;&nbsp;* When your hit goes into the outfield-run in the same way but in grass to the outside of the white line drawn from home plate to first base.</br>
&nbsp;&nbsp;&nbsp;* Prepare turn at first base by stepping on the bee with your left foot.</br>
&nbsp;&nbsp;&nbsp;* Continue toward second base, watching the ball.</br>
&nbsp;&nbsp;&nbsp;* Run until the ball is caught. Then either keep going or return first base. </br>
&nbsp;&nbsp;&nbsp;* When you are on base and a hit is made, run to the next base. Veer of the base line slightly before reaching the base, to make an easy turn, so you can make an easy turn. </br></br>

Younger players usually are not permitted to lead off the base. But be ready to run if there’s a hit. Here`s how: </br>

With one foot on the base, spread the feet comfortably with the amount to the side for balance. </br>

As the boll passes home plate, use the cross-over step and start toward the next base, which prepares you to run if the ball is hit or the catcher misses it. </br>

Never take your eye off the ball. </br></br>

<label class="bol">Offense </label></br>

In addition to throwing, catching and hitting, special skills like hunting, sliding, base coaching and batting order selection are also important. They can help your team play better and score more runs. </br></br>

<label class="bol">Bunting </label></br>

This is a method you can use to advance runners who are on base or to help them reach first base for a hit. </br></br>

<label class="bol">TO ADVANCE A RUNNER</label></br>
&nbsp;&nbsp;&nbsp;* Take your normal batting position.</br>
&nbsp;&nbsp;&nbsp;* As the pitcher releases the ball. Quickly turn to face the pitcher.</br>
&nbsp;&nbsp;&nbsp;* Hold the bat with a clenched fist on the barrel of the bat out in front of your body.</br>
&nbsp;&nbsp;&nbsp;* Let the ball hit the bat without moving it.</br>
&nbsp;&nbsp;&nbsp;* After the ball is bunted, run to first base.</br>
&nbsp;&nbsp;&nbsp;* Only bunt balls in the strike zone. </br></br>

<label class="bol">TO TRY FOR A BASE HIT</label></br>
&nbsp;&nbsp;&nbsp;* Wait until the last moment before you turn to bunt, so the other team will be surprised.</br>
&nbsp;&nbsp;&nbsp;* Bunt the ball either down the third or first baseline.</br>
&nbsp;&nbsp;&nbsp;* Run fast. You haven`t got much time. </br></br>

<label class="bol">SLIDING</label></br>

Sometimes you may decide to slide into a base. A slide can help you reach base safely, and avoid crashing into the person playing that base. </br>

To Slide, run toward the base in a straight line. When you are about ten feet away, drop down, extend one leg toward the base and bend the other under your body. Your speed will skid you toward the base, which your extended foot will reach before the ball does. You hope. </br>

Remember this: Once you start to slide don`t change your mind. To avoid getting hurt, keep on with the slide. When you practice sliding  wear sneakers or stocking feet. Practice on grass that’s been hosed down with lots of water. It`s wet, it`s wild, it`s fun, and it`s important. It may win you a game someday. </br></br>

<label class="bol">BASE COACHES</label></br>

A coach is often used at first base and third base to assist the runners. He advises the runner on the number of outs, where the ball is, whether or not to run on a fly ball, where the fielder is, and when to advance. A coach is a very valuable member on the team. Respect him. It is an honor to be asked to coach.  


