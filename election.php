<center><label class="bol"></br></br>MODEL CODE OF ELECTIONS FOR NATIONAL SPORTS FEDERATIONS AND THE NATIONAL OLYMPIC COMMITTEE</br></label>Name of the Federation:<strong><u>Amateur Baseball Federation of India</u></strong></br>Election of Office Bearers and other members of Executive Body</br></br>Election Bye-laws</center></br>
<p class="depart_p"><strong>1.     Short Title &amp; Definitions:</br></strong>
    (1)	These Bye-laws shall be called the 'Election Bye-laws' and shall govern the conduct of Election of Office Bearers and Members of the Executive Body.</br>
    (2)	In these Bye-laws, unless the context otherwise requires, -</br>
    	 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(a)&nbsp;'clause' and 'sub-clause' mean the clause and sub-clause of these Bye-laws;</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(b)&nbsp;'Executive members' means members of Executive Body;</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(c)&nbsp;'Form' means the Form appended to these Bye-laws;</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(d)&nbsp; Amateur Baseball Federation of India (ABFI) means Name of the Federation;</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(e)	'Office Bearers' as means members of the Executive Body</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(f)	'Rule' means the rule of Rules and Regulations of this Bye-laws;</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(g)	‘Website’ means the website of the National Sports Federation or the National Olympic   Committee.</br></br>
         
         <strong>2.	Executive Body:</br></strong>
         (1)       The Executive body may compromise preferably not more than <strong><i><u>17 members</br></br></u></i></strong>. 
         
         <strong>3.	Manner of Election:</br></br></strong>
         (1)	Office Bearers and members of Executive Body of ABFI shall be elected by secret ballot.</br>
         (2)	Election shall be held at the Annual General Council Meeting (AGM) or in a Special General Meeting in accordance with the procedure prescribed hereinafter, from amongst the representatives of the Permanent Member States/Union Territories/Boards/Institutions.</br></br>
         
         <strong>4.      Electoral College:</br></strong>
         (1)	Each   Permanent   Member   State/Union   Territory or other special units duly   affiliated   by ABFI as its Permanent Member shall have one vote at the elections of the Office Bearers and Executive Body Members.</br>
         (2)	For the purposes of sub-clause (1), each Permanent Member State/Union Territory or other special units shall be represented by two members authorized by the President or Secretary General/Secretary of the affiliated Permanent Member State/Union Territory; however, in case President/Secretary General / Secretary nominates different person(s), the person(s) authorized by the President shall be deemed to be the duly authorized person(s) irrespective of the date.</br>
         (3) Each Permanent Member State/Union Territory and each special unit shall intimate the name(s) of their representative(s)mentioned in sub-clauses (2) and (3), latest by 4.00 PM on 14th January, 2013; and such intimation shall be  addressed to the President/ Secretary General of ABFI on their letter head duly signed by President /Secretary General / Secretary of that member unit, so as to reach him on or before the aforesaid  date;  any change  in the name  of any  authorized representative after 4.00 PM on 14th January, 2012 or any intimation received thereafter shall be permitted only with the approval of the President of  ABFI.</br>
         (4)	The President / Secretary General of ABFI shall prepare the list of the authorised representatives of the Member States/Union Territories and other special units in Form 1, and circulate a copy of the list so prepared by him to all Member States/Union Territories/and/or other special units latest by <strong><u>5.00 PM on 14th January, 2013</u></strong> , by display, on the website of ABFI.</br>
         (5)	The President / Secretary General shall also furnish a duly authenticated copy of the list mentioned in sub-clause (5) to the Returning Officer as soon as may be after his appointment by the President of	ABFI  under clause (5)below.</br>
         (6) The National Olympic Committee shall ensure compliance with the provisions in the IOC Charter relating to the composition of the Executive Body, voting members and voting powers relating/pertaining to the National Olympic Committee.</br></br>
         
         <strong>5.       Returning Officer:</br></strong>
         As soon as may be before/after the issue of the Notice for the Annual General Meeting (AGM) or Special General Meeting, the President of ABFI shall nominate a person as Returning Officer, preferably a retired judge of a High Court for conduct of Election of Office Bearers and members of Executive Body, in accordance with the provisions of these Bye-laws.</br></br>
         
         <strong>6.       Nomination of Candidates:</br></strong>
         (1)	The nomination of a candidate for election as Office Bearer or Member of the Executive Body shall be made in Form 2.</br>
         (2)	The nomination of a candidate for election as Office Bearer or a Member of Executive Body  shall be proposed by one of the representatives of Member States/Union Territories/and/or other special unit whose name is included in the Electoral College list in Form 1, and also subscribed by 1 (one) such representative as seconder.</br>
         (3)	Each candidate shall be entitled to be nominated by not more than 2 (two) nomination papers.</br>
         (4)	Each Nomination Paper Shall be Delivered Personally to the Returning Officer in Person by the candidate himself/herself from <strong>21st January, 2013 to 23rd January, 2012 between 11.00 AM and 1.00 PM</strong>.</br>
         (5)	No person shall be eligible to nominate more than one candidate for the same post, either as proposer or seconder; and, if he so does, his/her signature on the nomination papers delivered second in point of time to the Returning Officer shall be deemed to be inoperative.</br>
         (6)	No person shall be permitted to withdraw his/her name as proposer/seconder, once the nomination paper subscribed by him/her has been delivered to Returning Officer.</br>
         (7)	As soon as may be after <strong><u>1:00 P.M. on 23rd January, 2013</u></strong>, the Returning Officer shall prepare a list of all nominations received by him, post wise, in Form 3; and publish the same in a conspicuous place in his office.</br></br>
         
        <strong>7.       Scrutiny of Nominations:</br></strong>
         (1) On <strong><u>23rd January, 2013 at 4.30 PM</u></strong> onwards the Returning Officer in his Office shall scrutinize each nomination paper, one by one, received by him, and determine its validity or otherwise.</br>
         (2) At the scrutiny of nominations, each candidate or one of his/her authorised representative shall have the right to be present and raise any objection in relation to nomination of a candidate for the post for which he/she has filed his/her nomination.</br>
         (3)	As soon as may be after the scrutiny of all nomination papers has been completed by him, the Returning Officer shall prepare a list of validly nominated candidates in Form 4. And the Contesting Candidates can take a copy from the Returning Officer.</br></br>
         
         <strong>8.	Withdrawal of candidatures:</br></strong>
         (1)	Each candidate whose nomination has been found valid on scrutiny shall be  entitled to withdraw his/her candidature, from <strong><u>11 AM upto 1:00 P.M. of   24th January, 2013</u></strong>.</br>
         (2)	Notice of withdrawal of candidature shall be submitted in Form 5, either by the candidate himself/herself or by a person duly authorised by him/her in writing.</br>
         (3)	Notice of withdrawal of candidature shall be final and shall not be allowed to be cancelled.</br>
         (5)	As soon as may be after <strong><u>1.00 PM on 24th January, 2013</u></strong>, the Returning Officer shall prepare the final list of contesting candidates in Form 6, display a copy of the said list in conspicuous place in his office and on NOC/NSF’s Website. And also furnish a copy of the same to each of the candidates/their authorised representatives, if they so demand.</br>
         (6)	The names of the contesting candidates in Form 6 shall be arranged, for each post, in alphabetical order according to English alphabets.</br></br>
         
         <strong>9.	Uncontested Returns; and Contested Elections</br></strong>
         (1)	Where the number of contesting candidates for any post or category of post, in Form 6 is equal to the number of posts to be filled, all such contesting candidates shall be deemed to be duly elected unopposed to those posts, and it shall not be necessary to take a poll for election to such post.</br>
         (2)	Where the number of contesting candidates for any post or category of post, in Form 6 is more than the number of posts to be filled, a poll shall be taken by secret ballot for those posts remaining unfilled.</br>
         (3)	The ballot papers for these posts remaining unfilled shall be in Form 7 to Form 13 to be made individually for each post.</br>
         (4)	The names of contesting candidates on the ballot papers shall appear in the same order as in Form 6.</br></br>
         
         <strong>10.	Poll:</br></strong>
         (1)	The Poll for the Posts Remaining Unfilled under clause 9(2) above shall be taken post-wise, on <strong><u>28th January, 2013 at 11.00 AM</u></strong> and shall commence at During the AGM or Special General Meeting at the Agenda Item Elections, in Hotel Pine Grove, Deoghat, Solan, H. P.</br>
         Each contesting candidate can nominate one person, (If he/ she so desires), only from amongst the Authorized Representatives list of PCI, to be present at the Poll (Polling station).</br>
         (2)	At the poll, each authorized representative of Member States/Union Territories/and/or other special units, whose name is included in the Electoral College list in Form 1, shall be entitled to -</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(i)	cast one vote for each of the posts remaining unfilled, where only one such seat is to be filled; </br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(ii)	cast as many votes as are the number of seats to be filled for any post, where more than one seat is to be filled.</br>
         Explanation: It is hereby clarified that for the purpose of filling four seats of Vice-Presidents, each voter can cast upto four votes, and, like-wise, for four seats of Joint Secretaries, and five seats of Members of Managing Committee, he/she can cast four and five votes respectively for the above mentioned four seats of Joint Secretaries and five seats of Members of Managing Committee.</br>
         4)	In case any voter casts his/her vote for more than one candidate, or for more than four or five candidates, as the case may be, his/her vote for that post shall be invalid.</br>
         (5)	Each voter shall be required, before he is supplied with a Ballot paper, to give his/her signature on the authenticated copy of the Electoral College list in Form 1 used by the Returning Officer for taking the poll.</br>
         (6)	The voter shall record his/her vote on the ballot paper in secrecy in a voting compartment specially provided for the purpose at the polling station.</br>
         7)	The voter shall record his/her vote on the ballot paper by placing a tick mark (√) against the name of the candidate of his/her choice, and any other mark, like (x) etc. or word, placed on the ballot paper by him/her shall render the ballot paper liable to rejection.</br>
         (8)	The tick mark (√) to indicate the vote shall be placed by the voter only by means of article specifically provided for the purpose by the Returning Officer, and a ballot paper marked otherwise by means of any pen, ball point pen, etc. by the voter shall render the ballot paper liable to rejection.</br>
         (9)	The ballot paper marked by a voter shall be deposited by him/her in a ballot box specially prepared and sealed by the Returning Officer and placed at such a conspicuous place in the polling station that it shall be constantly visible to all present in the polling station.</br>
         (10)	The Returning Officer shall close the poll at the appointed hour; however, all those voters who are present at the polling station at the appointed closing hour shall be entitled to vote even if the poll proceedings have to be continued for some more time.</br>
         (11)	After all the voters referred to in sub-clause (9) have voted, the Returning Officer shall close the ballot box and shall not permit thereafter any person to vote.</br>
         (12)	Polling will be done in below order:</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(a) President</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(b) Secretary General/General Secretary</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(c) Treasurer</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(d) Vice-Presidents</br>
         &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(e) Joint-Secretaries</br></br>
         
        <strong>11.	Counting of Votes:</br></strong>
         (1)	The Returning Officer shall take up the counting of votes as soon as may be after the polling for all posts  is complete.</br>
         (2)	Each contesting candidate can nominate one authorized representative (as clarified in S. No. 10 (2)) and He/She shall be entitled to be present at the place of counting of votes.</br>
         (3)	The Returning Officer shall take up the counting of votes, post-wise and category wise, where applicable.</br>
         (4)	Each ballot paper on which a vote has been recorded in accordance with sub-clauses (3) to (8) of clause (10) shall be treated as one vote for the candidate for whom it has been validly marked; and if any ballot paper has been marked in violation of any of the aforesaid provisions of sub-clauses (3) to (8) of clause (10) shall be rejected by the Returning Officer.</br>
         (5)	The votes validly cast for each of the contesting candidates shall be counted post-wise, and category-wise where applicable, and recorded in the descending order of the votes so cast for each candidate in Form 12.</br>
         (6)	The Returning Officer shall thereafter ascertain the result of counting and, subject to the provisions of Rule 4 (a) (II) of the Rules, the candidates who have secured the maximum no. of votes in the said descending order, post-wise and category wise, where applicable, equal to the no. of seats to be filled for each post or category of posts, where applicable, shall be deemed to have been duly elected to those posts.</br></br>
         
         <strong>12.	Declaration of Results:</br></strong>
         The names of contesting candidates who shall be deemed to have been elected at the election in accordance with sub-clause (6) of clause (11) shall be declared as having been duly elected to their respective posts, by the Returning Officer at the Annual General Meeting (AGM) or Special General Meeting in the Form 13 on the day on the day of voting.</p></br></br></br>
